/**
 * Loads the previous stated level (level1)
 * */
function load_level1(cursor) { 	
	/* clearing previous level */
	Game.moduleArray = [];
	
	var i;
	var counter = 0;
	Game.emptyBeforeStart = 34;
	
	/* If the game is starting from the begining (not loaded) */
	if(cursor == 0 || cursor == undefined) {
		/* Playing the audio */
		$("#"+Musics[Game.level].id).prop("currentTime",0);
		$("#"+Musics[Game.level].id).trigger("play");
		
		Game.scoreCounter = 0;
		Game.levelArrayCursor = 0;
		
		/* Loading empty cases before the start */
		for(i=0; i<Game.emptyBeforeStart; i++) {
			Game.moduleArray[i] = new Module(
										i * 2 * Measures.halfSquareWidth, 
										Measures.lineHeightCoef * Canvas.height + 1 * Measures.halfSquareHeight, 
										Levels[Game.level][0].translation, 
										EMPTY, 
										1, 
										true);
		}
	}
	
	/* Otherwise, the game is a loaded one */
	else {
		i = cursor;
		while(counter < 20 && i > 0) {
			Game.moduleArray.unshift(new Module(
										Canvas.width - Measures.halfSquareWidth - counter * 2 * Measures.halfSquareWidth, 
										Measures.lineHeightCoef * Canvas.height + Levels[Game.level][i].y * Measures.halfSquareHeight, 
										Levels[Game.level][i].translation, 
										Levels[Game.level][i].type, 
										1, 
										Levels[Game.level][i].isMounted));
										
			/* If it's an underline block, incrementing the counter */
			if(Levels[Game.level][i].type == EMPTY && Levels[Game.level][i].y == +1)
				counter++;
			i--;
		}
		
		/* If we couldn't fill the entire array with the modules existing, filling with empty ones */
		for(counter; counter<20; counter++)
			Game.moduleArray.unshift(new Module(
										Canvas.width - Measures.halfSquareWidth - counter * 2 * Measures.halfSquareWidth, 
										Measures.lineHeightCoef * Canvas.height + 1 * Measures.halfSquareHeight, 
										Levels[Game.level][0].translation, 
										EMPTY, 
										Levels[Game.level][0].alpha, 
										true));
	}
	
}


